fun main() {
//    val is for constants
    val userName: String = "Shubham"

//    var can be changes
    var city: String = "Visakhapatnam"
    city = "Hyderabad"

    val age: Int = 26

    // we don't need explicitly mention type 'Int'
    val salary = 23000
    println("My name is $userName. I am $age years old. My salary is $salary. I live in $city")
}